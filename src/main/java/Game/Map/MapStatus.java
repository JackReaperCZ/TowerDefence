package Game.Map;
/**
 * The MapStatus enum represents the current status of the game map.
 * <ul>
 *     <li>LOST: Indicates that the player has lost the game.</li>
 *     <li>WON: Indicates that the player has won the game.</li>
 *     <li>IN_PROGRESS: Indicates that the game is still in progress.</li>
 * </ul>
 */
public enum MapStatus {
    LOST,
    WON,
    IN_PROGRESS,
}
